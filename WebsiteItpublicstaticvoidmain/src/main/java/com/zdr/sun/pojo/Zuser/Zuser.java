package com.zdr.sun.pojo.Zuser;

public class Zuser {
    // 用户id
    private Integer zuserid;
    // 用户名
    private String zusername;
    // 用户密码
    private String zpassword;
    // 用户手机号
    private String zphone;
    // 用户邮箱
    private String zemail;

    public Integer getZuserid() {
	return zuserid;
    }

    public void setZuserid(Integer zuserid) {
	this.zuserid = zuserid;
    }

    public String getZusername() {
	return zusername;
    }

    public void setZusername(String zusername) {
	this.zusername = zusername;
    }

    public String getZpassword() {
	return zpassword;
    }

    public void setZpassword(String zpassword) {
	this.zpassword = zpassword;
    }

    public String getZphone() {
	return zphone;
    }

    public void setZphone(String zphone) {
	this.zphone = zphone;
    }

    public String getZemail() {
	return zemail;
    }

    public void setZemail(String zemail) {
	this.zemail = zemail;
    }

}
