package com.zdr.sun.pojo.cangku;

public class Cangkupan {

    public String pan;
    public String newid;
    public String type;
    public String panName;
    public String tiqu;
    public String mima;
    public String time;

    public String getPan() {
	return pan;
    }

    public void setPan(String pan) {
	this.pan = pan;
    }

    public String getNewid() {
	return newid;
    }

    public void setNewid(String newid) {
	this.newid = newid;
    }

    public String getType() {
	return type;
    }

    public void setType(String type) {
	this.type = type;
    }

    public String getPanName() {
	return panName;
    }

    public void setPanName(String panName) {
	this.panName = panName;
    }

    public String getTiqu() {
	return tiqu;
    }

    public void setTiqu(String tiqu) {
	this.tiqu = tiqu;
    }

    public String getMima() {
	return mima;
    }

    public void setMima(String mima) {
	this.mima = mima;
    }

    public String getTime() {
	return time;
    }

    public void setTime(String time) {
	this.time = time;
    }

}
